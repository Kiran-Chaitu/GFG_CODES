class Solution
{
    public:
    //Function to remove duplicates from unsorted linked list.
    Node * removeDuplicates( Node *head) 
    {
     // your code goes here
        if(head->next == NULL) return head;
        unordered_map<int , int> mp;
        vector<int> v;
        Node *temp = head;
        while(temp){
            mp[temp->data]++;
            if(mp[temp->data]==1) v.push_back(temp->data);
            temp = temp->next;
        }
        temp = head;
        int n=v.size();
        for(int i=0;i<n-1;i++){
            temp->data = v[i];
            temp = temp->next;
        }
        temp->data = v[n-1];
        temp->next=NULL;
        return head;
    }
};


