class Solution {

  public:
    vector<int> alternateSort(vector<int>& arr) {
        // Your code goes here
        vector<int> ans={};
        sort(arr.begin(),arr.end());
        ans = arr;
        int l=0,r=arr.size()-1,i=0;
        for(i=0;l<=r;i+=2){
            arr[i] = ans[r--];
            arr[i+1] = ans[l++];
        }
        return arr;
    }
};


